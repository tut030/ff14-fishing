def generate_welcome(elapsed: float, et_hour: int, weather_cn: str,
                     level_diff: int, level: int, first_time: bool,
                     zone: str = "",
                     rng: random.Random | None = None) -> tuple[str, int]:
    """
    生成欢迎回来文案。
    返回 (文案字符串, 休息经验)。不触发时返回 ("", 0)。
    """
    if rng is None:
        rng = random.Random()

    if first_time:
        return _FIRST_TIME, 0

    if elapsed < WELCOME_THRESHOLD:
        return "", 0

    lines = []

    # 1. 离开时长
    for threshold, msg in _TIME_AWAY:
        if elapsed < threshold:
            lines.append(msg)
            break

    # 2. 时段氛围
    if 6 <= et_hour < 12:
        pool = _MORNING
    elif 12 <= et_hour < 18:
        pool = _AFTERNOON
    elif 18 <= et_hour < 22:
        pool = _EVENING
    else:
        pool = _NIGHT
    lines.append(rng.choice(pool))

    # 3. 天气氛围 (有对应天气才加)
    for keyword, texts in _WEATHER.items():
        if keyword in weather_cn:
            lines.append(rng.choice(texts))
            break

    # 4. 位置类型氛围
    ztype = classify_zone(zone)
    zpool = _ZONE_POOLS.get(ztype, _ZONE_OTHER)
    lines.append(rng.choice(zpool))

    # 5. 肩头小惊喜 (30%)
    if rng.random() < 0.3:
        lines.append(rng.choice(_SHOULDER))

    # 6. 魔物遭遇 + 休息经验
    rest_xp = 0
    if level_diff >= SAFE_DIFF:
        lines.append(rng.choice(_SAFE))
        hours = min(elapsed / 3600, 24)
        rest_xp = min(int(REST_XP_BASE + hours * REST_XP_PER_HOUR), REST_XP_CAP)
        lines.append(f"（💤 安心休息加成: +{rest_xp} xp）")
    elif level_diff >= DANGER_DIFF:
        lines.append(rng.choice(_NEUTRAL))
    else:
        lines.append(rng.choice(_DANGER))

    return "\n".join(lines), rest_xp


def tug_text(tug: str, rng: random.Random) -> str:
    """按竿感等级随机选一条描写。"""
    pools = {
        "Light": TUG_LIGHT, "light": TUG_LIGHT,
        "Medium": TUG_MEDIUM, "medium": TUG_MEDIUM,
        "Heavy": TUG_HEAVY, "heavy": TUG_HEAVY,
        "Legendary": TUG_LEGENDARY,
    }
    pool = pools.get(tug, TUG_LIGHT)
    return rng.choice(pool)


def escape_text(tug: str, rng: random.Random) -> str:
    """脱钩描写。传说级有专属版。"""
    if tug == "Legendary":
        return rng.choice(ESCAPE_LEGENDARY)
    return rng.choice(ESCAPE_FLAVOR)


def ceremony_text(rng: random.Random) -> str:
    """传说级钓上来的揭晓瞬间。"""
    return rng.choice(CEREMONY_CATCH)


