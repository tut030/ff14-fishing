# ── 叉鱼空手/失手 ─────────────────────────────────────────
SPEAR_MISS = [
    "🔱 潜下去……水底只有沙纹和安静。此刻没有鱼。",
    "🔱 潜入水中……什么都没有。连水草都懒得动。",
    "🔱 水面下一片空旷。鱼都去哪了？",
]


def spear_dive_text(rng: "random.Random") -> str:
    """潜入水中的感官描写。"""
    return rng.choice(SPEAR_DIVE)


def spear_hit_text(size: str, rng: "random.Random") -> str:
    """按鱼影大小的叉中描写。"""
    pools = {
        "Small": SPEAR_HIT_SMALL,
        "Normal": SPEAR_HIT_NORMAL,
        "Large": SPEAR_HIT_LARGE,
        "UNKNOWN": SPEAR_HIT_NORMAL,
    }
    return rng.choice(pools.get(size, SPEAR_HIT_NORMAL))


def spear_miss_text(rng: "random.Random") -> str:
    """叉鱼空手。"""
    return rng.choice(SPEAR_MISS)
